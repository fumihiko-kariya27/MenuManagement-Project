DROP TABLE IF EXISTS cuisine_master;
/* メニューマスタ　作成 */
CREATE TABLE IF NOT EXISTS cuisine_master(cuisine_id INTEGER AUTO_INCREMENT PRIMARY KEY, cuisine_name VARCHAR(100) NOT NULL);
/* ユーザーマスタ　作成 */
CREATE TABLE IF NOT EXISTS user_master(user_id INTEGER AUTO_INCREMENT PRIMARY KEY, user_name VARCHAR(50) NOT NULL, password VARCHAR(50) NOT NULL, role VARCHAR(50) NOT NULL);